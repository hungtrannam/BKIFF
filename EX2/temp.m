function temp(fontsize, w,h,u)

if nargin == 0
    fontsize = 20;
elseif nargin == 3
    u = 'centimeters';
end

% Legend and Tick Labels
set(gca, ...
    'TickLabelInterpreter', 'latex', ...
    'FontSize'      , fontsize-1      ,...
    'FontWeight'    , 'normal' ); 
    %      ,...

% Labels and Title with LaTeX interpreter
set([get(gca,'xlabel'), get(gca,'ylabel'), get(gca,'zlabel'), get(gca,'title')], ...
    'FontSize'    , fontsize   , ...
    'FontWeight'  , 'bold'     , ...
    'Interpreter' , 'latex'    ); %'FontName'    , 'Times'    , ...

% Box plot text properties with LaTeX interpreter
set(findobj(gca, 'Type', 'text') , ...
    'FontSize'    , fontsize     , ...
    'FontWeight'  , 'bold'       , ...
    'Interpreter' , 'latex');
    % 'FontName'    , 'Times'      , ...

% Adjust Current Axes Properties
set(gca, ...
    'Box'         , 'on'      , ...
    'TickLength'  , [.03 .03] , ...
    'TickDir'     , 'in'     , ...
    'XMinorTick'  , 'on'     , ...
    'YMinorTick'  , 'off'     , ...
    'ZMinorTick'  , 'off'     , ...
    'XColor'      , [.3 .3 .3], ...
    'YColor'      , [.3 .3 .3], ...
    'LineWidth'   , 1.5,       ...
    'GridColor'   , [0.8 0.8 0.8], ...
    'GridAlpha'   , 0.3          , ... 
    'MinorGridColor', [0.9 0.9 0.9], ...
    'MinorGridAlpha', 0.1          , ...
    'XGrid'       , 'off'          , ...
    'YGrid'       , 'off'          , ...
    'ZGrid'       , 'off'          , ...
    'XMinorGrid'  , 'off'          , ...
    'YMinorGrid'  , 'off'          , ...
    'ZMinorGrid'  , 'off'          );


set(gcf,'Units', u);
screenpos = get(gcf,'Position');

% Adjust background color for a cleaner look
set(gcf, 'Color', [1 1 1]); % White background




p = 0.01;
set(gcf,...
  'Position',[screenpos(1:2) w h],...
  'PaperUnits',u,...
  'PaperPosition',[p*w p*h w h],...
  'PaperSize',[w*(1+2*p) h*(1+2*p)]);


axis tight;  
end
